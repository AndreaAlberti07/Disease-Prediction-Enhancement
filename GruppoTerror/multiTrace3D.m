function [handles,legendNames] = multiTrace3D(XData,YData,ZData,varargin)
    %%	Initialize parser
    p = inputParser;
    %%	Add required inputs
    addRequired(p,'xData', @(x) isrow(x));
    addRequired(p,'yData', @(x) isrow(x));
    addRequired(p,'zData', @(x) ismatrix(x));  
    %%	Add optional inputs
    addOptional(p,'cmap','civdis', @(x) ismember(x,["viridis","inferno",...
                  "magma","plasma","civdis","devon","oslo","tokyo"]));
    addOptional(p,'view',[-38 27], @(x) isrow(x));
    addOptional(p,'fAlpha',0.25, @(x) (isnumeric(x) && x>=0 &&  x<=1));
    addOptional(p,'lw',1.5, @(x) (isnumeric(x) && x>0));
    addOptional(p,'flipCmapLR',false, @(x) (x == true || x == false));
    %%	Parse
    parse(p,XData,YData,ZData,varargin{:});
    %%  Specify colormap    
    switch p.Results.cmap
        case 'viridis'
            clrs = viridis(numel(XData));
        case 'inferno'
            clrs = inferno(numel(XData));
        case 'plasma'
            clrs = plasma(numel(XData));
        case 'magma'
            clrs = magma(numel(XData));
        case 'civdis'
            clrs = civdis(numel(XData));
        case 'devon'
            clrs = devon(numel(XData));
        case 'oslo'
            clrs = oslo(numel(XData));
        case 'tokyo'
            clrs = tokyo(numel(XData));
    end   
    if p.Results.flipCmapLR
        clrs = fliplr(clrs);      	
    end
    %%  Generate plot
    handles = {};
    legendNames = {};
    for i = 1:numel(ZData(1,:)) 
        %   Plot i-th trace and color area under it 
        handles{i} = fill3([XData(i); XData(i);ones(numel(ZData(:,2)),1)*XData(i)],...
              YData([1 1:end end]),...
              [0; ZData(:,i); 0], clrs(i,:),...
              'FaceAlpha',p.Results.fAlpha,'EdgeColor',clrs(i,:),'LineStyle','-','LineWidth',p.Results.lw); %#ok<AGROW>
        hold on
        legendNames{i} =  num2str(XData(i)); %#ok<AGROW>
    end
    grid on
    view(p.Results.view(1),p.Results.view(2))    
    
